
public class DeckOfCards {
	public static void main(String[] args) {
		String spades = "\u2660";
		String hearts = "\u2665";
		String diamonds = "\u2666";
		String clubs = "\u2663";
		
		System.out.printf("A%s" + " " + "A%s" + " " + "A%s" + " " + "A%s"  , spades, hearts, diamonds, clubs);
		System.out.println();
		System.out.printf("K%s" + " " + "K%s" + " " + "K%s" + " " + "K%s"  , spades, hearts, diamonds, clubs);
		System.out.println();
		System.out.printf("Q%s" + " " + "Q%s" + " " + "Q%s" + " " + "Q%s"  , spades, hearts, diamonds, clubs);
		System.out.println();
		System.out.printf("J%s" + " " + "J%s" + " " + "J%s" + " " + "J%s"  , spades, hearts, diamonds, clubs);
		
		
	}
}
